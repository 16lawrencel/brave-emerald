/*
All form changes are checked for the base species!
Putting a form change on a form has no effect

FORM_ITEM_USE:
    Changes any pokemon with a matching base species (even if
    it is a form) to the target species. 
    param1 = item to use
    param1 = ability to check for (none means any)

FORM_ITEM_USE_ONE_WAY:
    FORM_ITEM_USE_ONE_WAY differs in that the change will occur 
    from param2 to target. This is needed because FORM_ITEM_USE
    represents a many-to-one relationship, while Shaymin and
    Hoopa form changes have a one-to-one relationship
    param1 = item to use
    param2 = original species

FORM_ITEM_HOLD:
    param1 = item to hold
    param2 = ability to check for (none means any)

FORM_MOVE:
    param1 = move to check for

FORM_STATUS: Unimplemented
    param1 = status to check for

FORM_ELAPSED_DAYS: Unimplemented
    param1 = how many days have passed since the last form change

If a form change's target is the same as the base, this means that 
it will change any form to the base
*/

